%%%%% 利用contour函数绘制函数peak(100)的等高线图
%%% 首先生成（x,y）坐标，然后生成y坐标，最后绘制等高线图
[x,y]=meshgrid(1:100,1:100); 	% 设置等间距步长
z=peaks(100);						% 函数图形表达式
contour(x,y,z)						% 绘制等高线
%%% 得到如图5-24所示的图形

