%%%%%%%%%%%%%%%%%% 本代码主要用于绘制双Y轴坐标图形 %%%%%%%%%%%%%%%%
x1=linspace(-7,7,100);		%线性设置x1等间步长，范围为［-7, 7］
y1=sin(x1);					%函数表达式，自变量ｘ1与变量ｙ1之间的关系
x2=linspace(-7,7,100);		%线性设置x1等间步长，范围为［-7, 7］
y2=cos(x2);					%函数表达式，自变量ｘ2与变量ｙ2之间的关系
plotyy(x1,y1,x2,y2)				%绘制双Y轴坐标图形，对应的函数曲线分别为ｘ1-ｙ1，ｘ2-ｙ2
