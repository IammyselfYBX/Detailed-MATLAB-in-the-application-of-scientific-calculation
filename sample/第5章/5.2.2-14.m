%%%%%%%%%%%%%%%%%%%% 本代码主要用于绘制矢量图 %%%%%%%%%%%%%%%%%%
% 设置图形显示网格，范围为［－２，２］，［－１，１］
[x,y]=meshgrid(-2:0.2:2,-1:0.15:1);	
%  建立函数关系表达式	
z=x.*exp(-x.^2-y.^2);
[px,py]=gradient(z,0.2,0.15);			% 设置ｚ轴的梯度表达式
contour(x,y,z)								% 绘制等高线图
hold on										% 图形保持
quiver(x,y,px,py)							% 绘制ｘ，ｙ轴的矢量图
hold off									% 关闭图形保持
axis image
