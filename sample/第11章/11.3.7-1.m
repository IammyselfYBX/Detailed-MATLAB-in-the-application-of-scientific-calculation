%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 以下代码用于使用edge函数对图片的轮廓进行分析
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 使用imread函数打开MATLAB系统搜索路径范围内一个名为coil.jpg的图片
b=imread('coil','jpg');
% 绘制出原始的灰度图形，得到如图11-24所示的图形
imshow(b)
% 使用edge函数对图形的边界进行提取，采用的算法是'sobel'
bw1=edge(b,'sobel');
% 绘制出边界提取后的图形，得到如图11-25所示的图形
imshow(bw1)
% 使用edge函数对图形的边界进行提取，采用的算法是'canny'
bw2=edge(b,'canny');
% 绘制出边界提取后的图形，得到如图11-26所示的图形
imshow(bw2)

