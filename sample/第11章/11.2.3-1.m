% 求解得到ib的值
ib=I(2);
% 求解得到ic的值
ic=I(3);
% 求解得到i3=ia-ib的值
i3=ia-ib
% 求解得到u4=R4*ib的值
u4=R4*ib
% 求解得到u7=R7*ic的值
u7=R7*ic
