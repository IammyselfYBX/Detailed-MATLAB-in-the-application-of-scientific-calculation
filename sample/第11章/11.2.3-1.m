% 清空当前工作区间中所有变量
clear,close all, format compact
% 根据题目中的已知条件，对各个元件进行赋值
R1=2;R2=4;R3=12;R4=4;R5=12;R6=4;R7=2;
% 输出提示符号“question 1: ”
display('question 1:')
% 对方程组中的元素a11，a12，a13分别进行赋值
a11=R1+R2+R3;a12=-R3;a13=0;
% 对方程组中的元素a21，a22，a23分别进行赋值
a21=-R3;a22=R3+R4+R5;a23=-R5;
% 对方程组中的元素a31，a32，a33分别进行赋值
a31=0;a32=-R5;a33=R5+R6+R7;
% 对方程组中的元素b1，b2，b3分别进行赋值
b1=1;b2=0;b3=0;
% 对us进行赋值
us=10;
% 构建方程组的系数矩阵
