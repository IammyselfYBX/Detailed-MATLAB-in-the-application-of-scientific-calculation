%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 以下代码用于使用傅里叶变换实现对图像的处理
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 在MATLAB路径中读取一个名为a.jpg的图片
rgb=imread('a.jpg');
% 使用rgb2gray函数将图片转换成灰度图片
I=rgb2gray(rgb);
% 创建图形1
figure(1);
% 在图形1中显示图片1
imshow(I)
% 创建图形2
figure(2);
% 对图形矩阵进行二维傅里叶变换
b=fftshift(fft2(I));
% 显示经过傅里叶变换后的图片，并且添加色条
imshow(log(abs(b)),[]),colormap(jet(64)),colorbar
% 得到如图11-20所示的图形
