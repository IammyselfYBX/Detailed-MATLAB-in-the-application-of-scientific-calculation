%%%%%%%%%%%%%%%% 本代码主要用来实现用三次样条算法来求解函数的值%%%%%%%%%%
% 确定变量x的范围为-4到4
x=-4:4;
% 确定函数y的数据值
y=[0 .15 1.12 2.36 2.36 1.46 .49 .06 0];
% 利用spline函数来进行三次样条插值：从相同数据集y中获取[0,y,0的内插值]
cs=spline(x,[0 y 0]);
% 利用linspace函数进行三次样条插值
xx=linspace(-4,4,101);
% 绘制出两种三次样条插值的函数图形
plot(x,y,'o',xx,ppval(cs,xx),'-');
