v1=[1 2 3]			%通过系数向量构造多项式1
v2=[2 3 4]			%通过系数向量构造多项式2
p1=polyder(v1)		%求解多项式1的导数
p2=polyder(v2)		%求解多项式2的导数
p3=polyder(conv(v1,v2))	%求解多项式1与多项式2乘积的导数
[p4,d4]=polyder(v2,v1)	%求解多项式1除以多项式2商的导数
Y1=poly2sym(p1)		%将多项式1的系数向量转换成表达式
Y2=poly2sym(p2)		%将多项式2的系数向量转换成表达式
Y3=poly2sym(p3)		%将多项式1与多项式2乘积的系数向量转换成表达式
Y4=poly2sym(p4)		%将多项式1除以多项式2的商的系数向量转换成表达式
D4=poly2sym(d4)		%将多项式1除以多项式2的余数的系数向量转换成表达式