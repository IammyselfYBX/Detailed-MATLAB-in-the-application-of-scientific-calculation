m=[82,54.8,50.9,68.4]*1e-3;  % 小车的质量
k=[787.76,251.93,330.47,429.8];  % 弹性系数
kk=k+[k(2:end),0];
A=[kk(1),-k(2),0,0;-k(2),kk(2),-k(3),0;
    0,-k(3),kk(3),-k(4);0,0,-k(4),kk(4)];  % 构造系数矩阵的分子部分
A=bsxfun(@rdivide,A,m');  % 构造系数向量，等价于A=A./repmat(m',1,4)
[V,D]=eig(A);  % 特征值和特征向量的求解
Lambda=sqrt(diag(D))/(2*pi)  % 求谐振动的频率
